function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function mul(a, b) {
    return a * b;
}

function div(a, b) {
    return a / b;
}

console.log("1 + 2 = ", sum(1, 2));
console.log("10 - 6 = ", sub(10, 5));
console.log("5 * 5 = ", mul(5, 5));
console.log("10 / 2 = ", div(10, 2));
